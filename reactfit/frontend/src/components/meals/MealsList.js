import React, { Component } from "react";
import { Link } from "react-router-dom";
import Chart from "./Chart";
import AuthService from "../../services/AuthService";
import MealService from "../../services/MealService";

const Meal = (props) => (
  <tr>
    <td>{props.meal.description}</td>
    <td>{props.meal.calories}</td>
    <td>{props.meal.date.substring(0, 10)}</td>
    <td>
      <Link to={"/meal-update/" + props.meal._id} style={{ color: " #a04949" }}>
        Edit
      </Link>{" "}
      |{" "}
      <a
        href="#"
        onClick={() => {
          props.deleteMeal(props.meal._id);
          window.location.reload(false);
        }}
        style={{ color: " #a04949" }}
      >
        Delete
      </a>
    </td>
  </tr>
);

export default class MealsList extends Component {
  constructor(props) {
    super(props);

    this.deleteMeal = this.deleteMeal.bind(this);

    this.state = { meals: [] };
  }

  componentDidMount() {
    var currentUserId = AuthService.getCurrentUser()
      ? AuthService.getCurrentUser().id
      : null;
    MealService.getMeals(currentUserId)
      .then((response) => {
        this.setState({ meals: response.data });
      })
      .catch((error) => {
        console.log(error);
        this.setState({
          content:
            (error.response &&
              error.response.data &&
              error.response.data.message) ||
            error.message ||
            error.toString(),
        });
      });
  }

  deleteMeal(id) {
    MealService.deleteMeal(id).then((response) => {
      console.log("DA");
      console.log(response.data);
    });

    this.setState({
      meals: this.state.meals.filter((el) => el._id !== id),
    });
  }

  mealList() {
    return this.state.meals.map((currentmeal) => {
      return (
        <Meal
          meal={currentmeal}
          deleteMeal={this.deleteMeal}
          key={currentmeal._id}
        />
      );
    });
  }

  render() {
    return (
      <div>
        <div className="containerr">
          <div className="cardd border-0 shadow my-4">
            <div className="card-body p-5">
              <h3 style={{ textAlign: "center", marginBottom: "15px" }}>
                Food Tracker
              </h3>
              <table className="table" style={{ textAlign: "center" }}>
                <thead
                  className="thead"
                  style={{ backgroundColor: "rgb(160 73 73 / 68%)" }}
                >
                  <tr>
                    <th>Description</th>
                    <th>Calories</th>
                    <th>Date</th>
                    <th>Actions</th>
                  </tr>
                </thead>
                <tbody>{this.mealList()}</tbody>
              </table>
            </div>
          </div>
        </div>
        <div className="containerr">
          <div
            className="cardd border-0 shadow my-2"
            style={{ padding: "2rem" }}
          >
            <div className="card-body p-1"></div>
            <Chart />
          </div>
        </div>
      </div>
    );
  }
}
